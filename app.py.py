# 11. FUNCION SIGMOIDE
# ============================================

def sigmoid(z):

    return 1 / (1 + np.exp(-z))

# ============================================
# 12. FUNCION LOSS
# ============================================

def loss(X, y, theta):

    m = len(y)

    h = sigmoid(X @ theta)

    epsilon = 1e-5

    return -(1/m) * np.sum(
        y*np.log(h + epsilon)
        +
        (1-y)*np.log(1-h + epsilon)
    )

# ============================================
# 13. ENTRENAMIENTO IA
# ============================================

def gradient_descent(
    X,
    y,
    theta,
    lr=0.01,
    epochs=3000
):

    m = len(y)

    historial = []

    for i in range(epochs):

        h = sigmoid(X @ theta)

        gradient = (1/m) * (X.T @ (h - y))

        theta -= lr * gradient

        l = loss(X, y, theta)

        historial.append(l)

        if i % 500 == 0:

            print(f'Epoch {i} Loss: {l}')

    return theta, historial

# ============================================
# 14. ENTRENAR MODELO
