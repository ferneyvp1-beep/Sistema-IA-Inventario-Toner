def sistema_toner():

    tabla = inventario[[
        'Serial',
        'Modelo Impresora',
        'Ciudad',
        'ProbabilidadCambio',
        'EstadoCambio',
        'NivelCritico'
    ]]

    return tabla.fillna('NO REQUIERE CAMBIO')

# ============================================
# 28. BUSCAR IMPRESORA
# ============================================

def buscar_impresora(serial):

    resultado = inventario[
        inventario['Serial']
        .astype(str)
        .str.contains(
            serial,
            case=False,
            na=False
        )
    ]

    return resultado.fillna(
        'NO REQUIERE CAMBIO'
    )

